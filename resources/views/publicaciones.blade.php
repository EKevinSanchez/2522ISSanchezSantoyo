                    <h3>Clasificacion de Sistemas de información</h3>
                </div>
                <div class="subtitle">
                    Según su arquitectura
                </div>
                <div class="content">
                    Tomando en cuenta la arquitectura de los sistemas de información, se puede clasificar de la siguiente forma:
                    <br>
                    <li>Sitemas de dos niveles, archvivo-servidor: sus funciones se limitan habitualmente por el almacenamiento de datos y el procesamiento de datos 
                    se realiza unicamente del lado del cliente</li>
                    <li>Sitemas de dos niveles, cliente-servidor: los calculos se realizan en el servidor por medio del SGBD bajo la protección del sistema operativo
                    del servidor</li>
                    <li>Sistemas de muchos niveles: las funciones de procesamiento de datos son realizados en uno o varios servidores especiales, esto permite 
                    dividir las funciones de almacenamiento, procesamiento y presentación de datos para un uso mas efectivo.</li>
