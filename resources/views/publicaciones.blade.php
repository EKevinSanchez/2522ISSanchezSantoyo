                    <h3>Sistemas de información </h3>
                </div>
                <div class="subtitle">
                    ¿Qué es un sistema de información?
                    <br>
                </div>
                <div class="content">
                    Es un conjunto de componentes interrelacionados que trabajan juntos para recopilar, procesar, almacenar y difundir informacion para apoyar la toma de decisiones.
                    <br>
                    En el ambito empresarial, acualmente se dependen en gran medida, de sistemas para poder gestionar sus operaciones y asi poder tomar desiciones que sean mas beneficiosas.
                    <br>
                    <div class="img-1">
                        <img src="images/publicaciones/sistemas1.png" alt="">
                    </div>
                    <br>
                    Un sistema esta formado con los sigueintes componentes:
                    <br>
                    <div class="img-1_1">
                        <img src="images/publicaciones/componentes.jpg" alt="">
                    </div>
                    <li>Hardware: utilizan hardware local que es donde estara corriendo el sistema de informacion.</li>
                    <li>Software: son aquellos programas que son usados para la administracion, procesamiento y analisis.</li>
                    <li>Bases de datos: es donde sera alamacenado los recursos que seran utilizados en el sistema; estos recursos estan organizados en tablas y archivos</li>
                    <li>Red: es usada para conectar diferentes recursos entre si, tanto puede ser usada de forma interna en una organizacion o de forma global con el uso del internet.</li>
                    <li>Procedimientos: son aquellos que describen como de procesa y analiza los datos y recursos especificos para obtener las respuestas de las consultas realizadas.</li>
                

                    <br>
                    Los sistemas de informacion se pueden dividir en los siguientes tipos:
                    <br>
                    <li>Sistema de gestion de bases de datos (DBMS): es una combinacion que permite la administracion y su analisis.</li>
                    <li>Sistemas de informacion geografica(SIG): permiten gestionar y analizar todo tipo de datos geograficos.</li>
                    <li>Sistemas de decisiones (DSS): son sistemas utilizados en la gerencia de una empresa, para recopilar informacion de una amplia gama de fuentes y procesos, esto les ayudara para la toma de desiciones y obtener una solucion de algun problema que se presente</li>
                    <li>Sistemas ejecutivos (EIS): proporciona acceso rapido a informes y procesos resumidos, estos informes son recopilados de todos los departamentos de la organizacion.</li>

                </div>
