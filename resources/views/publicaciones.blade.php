                    <h3>Cliente-Servidor</h3>
                </div>
                <div class="subtitle">
                    Conceptos de cliente y servidor 
                </div>
                <div class="content">
                    Dentro del mundo de la informática se emplea un modelo que permite establecer una estructura predeterminada para las aplicaciones respetivas a los servicios y los distintos proveedores, este se le conoce como "Cliente-Servidor".
                    Esta arquitectura tiene como principio la agrupacion de todo tipo de aplicaciones en dos, los clientes y los servidores.
                    <br>
                    El cliente es aquel dispositivo que requiere ciertos servicios y lo consume, esto se realiza normalmete a través de alguna red (ya sea de forma local o a través de internet). 
                    Los clientes pueden ser tanto un equipo de computo como una aplicación, como seria un navegador web, una aplicacion de escritorio, una aplicacion de movil, etc.
                    <br>
                    <div class="img-1_1">
                        <img src="images/publicaciones/cliente.jpg" alt="90px">
                    </div>
                    El servidor es aquel equipo que brinda los servicios por medio de la red. Estos pueden ser tanto fisicos como virtuales(un servicio de nube). 
                    <br>
                    Algunos de los tipos de servidores mas usados son:
                    <br>
                    <li><b>Servidor Web: </b>almacena y organiza el contenido de las paginas web y lo proporciona al usuario a través del navegador web del cliente</li>
                    <li><b>DNS: </b>este se encarga de relacionar una dirección de dominio con su direccion IP correspondiente </li>
                    <li><b>Proxy Server: </b>es un servidor de puerta de entrada, se encarga de conectar la red en donde esta el cliente a un sistema externo para que realice la solicitud de conexion,
                    mejor rendimiento y accesibilidad</li>
                    <li><b>Servidor de correo electrónico: </b> este se encarga del flujo de correo electronico de los usuarios.</li>
                    <li><b>Servidor FTP: </b>es usado para transferir archivos entre un cliente y un servidor, para que el servidor permita la posilibilidad de la descarga de esos archivos por parte de otros clientes.</li>
                </div>
