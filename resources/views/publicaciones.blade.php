                    <h3>Sistemas de computadora centrales y dedicadas</h3>
                </div>
                <div class="subtitle">
                    Computadoras centrales 
                </div>
                <div class="content">
                    Es usado principalmente en el procesamiento de una gran cantidad de informacion. Actualmente este tipo de sistemas son usados para brindar servicios web ya que podian dar una potencia similar a la de cientos
                    de computadoras personales.
                    <br>
                    El tipo de equipos donde es usado este sistema de caracterisan por su gran velocidad en su CPU como por su gran capacidad de memoria interna, una gran capacidad de almacenamiento externo, entre muchos otros aspecto.
                    <br>
                    Este tipo de maquina puede funcionar durante años sin problemas ni interrupciones (ya que estan trabajando 24/7) y las reparaciones que sean necesarias pueden ser realizadas mientras esta en funcionamiento.
                    <br>
                    <div class="img-1_1">
                        <img src="images/publicaciones/central.jpg" alt="">
                    </div>
                    Ya que pueden soportar miles de usuarios de manera simultanea que son conectados por medio terminales virtuales, un entotorno de trabajo versátil y simplificado, gran velocidad de procesamiento a un bajo costo 
                    pueden ser empleadas para las siguientes aplilcaciones:
                    <br>
                    <li>Administracion y soporte en grandes redes, asi como servidor de aplicaciones web y bases de datos dinamicas para acceso remoto</li>
                    <li>Investigaciones cientificas tanto en bajo, medio y en algunas situaciones aisladas en alto nivel.</li>
                    <li>En estaciones meteoroligcas, permitiendo el desarrollo de animaciones sobre el estado del clima y las posibles situaciones a lo largo del día</li>
                    <li>En ingeniería civil, en el desarrollo de planos y modelos de construccion mas eficientes, edificios capaces de soportar multiples climas adversos, asi como el diseño para un mejor aproechamiento de los recursos naturales 
                    (luz natural, espacio, climatizacion, etc.)</li>
