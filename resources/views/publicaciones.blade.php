                    <h3>Protocolos de comunicación en red</h3>
                </div>
                <div class="subtitle">
                    ¿Qué son los protocolos de comunicación en red?
                </div>
                <div class="content">
                    Los protocolos definen un formato y conjunto de reglas comunes para intercambiar mensajes entre los dispositivos de una red.
                    <br>
                    <div class="img-1_1">
                        <img src="images/publicaciones/protocolos.png" alt="">
                    </div>
                    Algunos de los protocolos mas comunes son:
                    <li>Hypertext Transfer Protocol (HTTP): rige la forma en que interactuan un servidor web y el cliente web.</li>
                    <li>Protocol de Control de Transmicion (TCP): administra las conversaciones individuales.</li>
                    <li>Protocolo de Internet (IP): encapsula los segmentos TCP en paquetes, asigna direcciones y entrega al host de destino.</li>
                    <li>Ethernet: permite la comunicación a través de un enlace de datos y la transmicion fisica de datos en la red.</li>
                    La interaccion de estos protocolos permiten la comunicacion entre un servidor web y un cliente web.
