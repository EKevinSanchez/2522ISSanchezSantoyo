                <div class="title">
                    <h3>Arquitectura Cliente/Servidor</h3>
                </div>
                <div class="subtitle">
                    Descipción de la arquitectura cliente/servidor
                </div>
                <div class="content">
                    Consiste basicamente en un cliente que realiza a un servidor, el cual le da una respuesta. Esta idea puede ser aplicada en programas que se 
                    ejecutan en una sola computadora, se aprovechan en mayor medida sus caracteristicas en un sistema operativo multiusuario, siendo distribuido en una
                    red de computadoras. La interaccione entre el cliente y servidor es el soporte para la mayoy parte de la comunicación por redes.
                    <br>
                    <div class="img-1_1">
                        <img src="images/publicaciones/Cliente_Servidor.jpg" alt="">
                    </div>
                    La forma en como se desarrolla es, cuando el servidor espera pasivamente las peticiones desde un puerto conocido, el cual ha sido reservado para el servicio, 
                    mientras que el cliente mediante su sistema operativo enviar una peticion mediante un puerto no usado y espera la respuesta.
                    <br>
                    <br>
                    <b>Clasificacion de los servidores</b>
                    <br>
                    Los servidores pueden clasificarse de la siguiente manera:
                    <br>
                    <li>Servidores de estado</li> 
                    <li>Servidores sin estado</li>
                    <li>Servidores concurrentes</li>
                    <br>
                    <b>Algunas de las caracteristicas sobre esta arquitectura son:</b>
                    <br>
                    <li>Combinacion del cliente que interactua con el usuatio y el servidor que interactua con los recursos solicitados</li>
                    <li>Los requerimiento de computo entre el cliente y el servidor son distintas</li>
                    <li>Existe una relacion entre procesos distintos, los cuales pueden ser ejecutados en la misma o diferentes maquinas dentro de la red</li>
                    <li>El servidor puede dar servicio a muchos clientes, regulando el acceso a los recursos compartidos</li>
                    <li>En este tipo de sistemas el concepto de escalabilidad tanto horizontal como vertical es aplicable </li>
                    <br>
                    <b>Ventajas y desventajas del Cliente/Servidor</b>
                    <br>
                    Ventajas:
                    <li>Facilita la integración entre distintos sistemas y la posibilida de compartit la información</li>
                    <li>Favorece el uso de interfaces graficas, permite que sea mas intuitivos para el usuario</li>
                    <li>Contar con una estructura inherente modular, facilita la integracion de nuevas tecnologias y el crecimiento de la infraestructura</li>
                    Desventajas:
                    <li>El mantenimiento de los sistemas es mas complejo.</li>
                    <li>Cuenta con escasas herramientas de administracion y ajuste del desempeño.</li>
                    <li>Debe contar con mecanismos generales para que logren existir en diferentes plataformas.</li>
                    <li>Contar con estrategias para el manejo de errores y para la mantener la consistencia de los datos.</li>
                    <li>Problemas con el desempeño debido a congestion en la red, dificultad de trafico de datos, entre otros.</li>
